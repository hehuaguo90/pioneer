package com.wellsys.common.utils;

import java.util.List;

public class DataDictMenuModel {
	// Fields

	/**
		 * 
		 */
	private String dataDictNo;
	private String name;
	private String nameInitAbbr;
	private String gbNo;
	private String parentNo;
	private List<DataDictMenuModel> children;

	public DataDictMenuModel() {
	}

	public DataDictMenuModel(String dataDictNo, String name,
			String nameInitAbbr, String gbNo, String parentNo,
			List<DataDictMenuModel> children) {
		this.dataDictNo = dataDictNo;
		this.name = name;
		this.nameInitAbbr = nameInitAbbr;
		this.gbNo = gbNo;
		this.parentNo = parentNo;
		this.children = children;
	}

	public String getDataDictNo() {
		return dataDictNo;
	}

	public void setDataDictNo(String dataDictNo) {
		this.dataDictNo = dataDictNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNameInitAbbr() {
		return nameInitAbbr;
	}

	public void setNameInitAbbr(String nameInitAbbr) {
		this.nameInitAbbr = nameInitAbbr;
	}

	public String getGbNo() {
		return gbNo;
	}

	public void setGbNo(String gbNo) {
		this.gbNo = gbNo;
	}

	public String getParentNo() {
		return parentNo;
	}

	public void setParentNo(String parentNo) {
		this.parentNo = parentNo;
	}

	public List<DataDictMenuModel> getChildren() {
		return children;
	}

	public void setChildren(List<DataDictMenuModel> children) {
		this.children = children;
	}

}
